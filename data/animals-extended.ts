// 동물 데이터를 카테고리별로 분류하는 헬퍼 함수
export const getAnimalsByCategory = (category: AnimalData['category']) => {
  return animalsData.filter(animal => animal.category === category);
};

// 희귀도별로 분류하는 헬퍼 함수
export const getAnimalsByRarity = (rarity: AnimalData['rarity']) => {
  return animalsData.filter(animal => animal.rarity === rarity);
};

// 레벨별로 해금 가능한 동물 목록
export const getUnlockedAnimals = (playerLevel: number) => {
  return animalsData.filter(animal => animal.unlock_level <= playerLevel);
};

// 동물 검색 함수
export const searchAnimals = (query: string) => {
  const lowercaseQuery = query.toLowerCase();
  return animalsData.filter(animal => 
    animal.name.toLowerCase().includes(lowercaseQuery) ||
    animal.korean_name.includes(query) ||
    animal.description.toLowerCase().includes(lowercaseQuery) ||
    animal.kid_description.includes(query)
  );
};